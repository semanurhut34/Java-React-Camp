package dayOneTwoLectureOne;

public class Instructor {

	int id;
	String firstName;
	String lastName;
	int age;
	String certificate;

	public Instructor(int id, String firstName, String lastName, int age, String certificate) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.lastName = lastName;
		this.age = age;
		this.certificate = certificate;
	}

}
